/**
  * @brief This function handles ADC1 and ADC2 interrupts.
  */
void ADC1_2_IRQHandler(void)
{
  /* USER CODE BEGIN ADC1_2_IRQn 0 */

  /* USER CODE END ADC1_2_IRQn 0 */
  HAL_ADC_IRQHandler(&hadc1);
  /* USER CODE BEGIN ADC1_2_IRQn 1 */
  if(HAL_GetTick() - 100 >= prev_tick){
    raw = HAL_ADC_GetValue(&hadc1);
    if((min_raw == 0 && has_changed_min == 0) || raw < min_raw) {
      min_raw = raw;
      has_changed_min = 1;
    }
    if((min_raw != raw && max_raw < raw) || (max_raw == 4095 && min_raw != raw && has_changed_max == 0)) {
      max_raw = raw;
      has_changed_max = 1;
    }
    prev_tick = HAL_GetTick();
  }
  HAL_ADC_Start_IT(&hadc1);
  /* USER CODE END ADC1_2_IRQn 1 */
}

/**
  * @brief This function handles TIM3 global interrupt.
  */
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */

  /* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */

  unsigned char buffer[100] = {'\0'};
  float normalized = ((float)(raw - min_raw) * 100) / (max_raw - min_raw);
  int int_norm = (int) normalized;
  sprintf(buffer, "raw = %d and normalized = %d.%d and min = %d and max = %d\n", raw, int_norm, (int) ((normalized - int_norm) * 1000), min_raw, max_raw);

  /* sprintf(buffer, "raw = %d and normalized = %f\n", raw, (float)4.213); */
  HAL_UART_Transmit(&huart3, buffer, sizeof(unsigned char) * 100, 1000);
  /* USER CODE END TIM3_IRQn 1 */
}

